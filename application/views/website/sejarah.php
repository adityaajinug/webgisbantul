              <p style="text-align: justify;">
                Bantul memang tak bisa dilepaskan dari sejarah Yogyakarta sebagai kota perjuangan dan sejarah
                perjuangan Indonesia pada umumnya. Bantul menyimpan banyak kisah kepahlawanan. Antara lain, perlawanan
                Pangeran Mangkubumi di Ambar Ketawang dan upaya pertahanan Sultan Agung di Pleret. Perjuangan Pangeran
                Diponegoro di Selarong. Kisah perjuangan pioner penerbangan Indonesia yaitu Adisucipto, pesawat yang
                ditumpanginya jatuh ditembak Belanda di Desa Ngoto. Sebuah peristiwa yang penting dicatat adalah
                Perang Gerilya melawan pasukan Belanda yang dipimpin oleh Jenderal Sudirman (1948) yang banyak
                bergerak di sekitar wilayah Bantul. Wilayah ini pula yang menjadi basis, &quot;Serangan Oemoem 1
                Maret&quot; (1949) yang dicetuskan oleh Sri Sultan Hamengkubuwono IX.</p>
              <p style="text-align: justify;">
                Tolok awal pembentukan wilayah Kabupaten Bantul adalah perjuangan gigih Pangeran Diponegoro melawan
                penjajah bermarkas di Selarong sejak tahun 1825 hingga 1830. Seusai meredam perjuangan Diponegoro,
                Pemeritah Hindia Belanda kemudian membentuk komisi khusus untuk menangani daerah Vortenlanden yang
                antara lain bertugas menangani pemerintahan daerah Mataram, Pajang, Sokawati, dan Gunung Kidul.
                Kontrak kasunanan Surakarta dengan Yogyakarta dilakukan baik hal pembagian wilayah maupun pembayaran
                ongkos perang, penyerahan pemimpin pemberontak, dan pembentukan wilayah administratif.</p>
              <p style="text-align: justify;">
                Tanggal 26 dan 31 Maret 1831 Pemerintah Hindia Belanda dan Sultan Yogyakarta mengadakan kontrak kerja
                sama tentang pembagian wilayah administratif baru dalam Kasultanan disertai penetapan jabatan kepala
                wilayahnya. Saat itu Kasultanan Yogyakarta dibagi menjadi tiga kabupaten
                yaitu&nbsp;<strong>Bantulkarang</strong>&nbsp;untuk kawasan
                selatan,&nbsp;<strong>Denggung</strong>&nbsp;untuk kawasan utara,
                dan<strong>Kalasan</strong>&nbsp;untuk kawasan timur. Menindaklanjuti pembagian wilayah baru
                Kasultanan Yogyakarta, tanggal&nbsp;<strong>20 Juli 1831</strong>&nbsp;atau&nbsp;<strong>Rabu Kliwon
                  10 sapar tahun Dal 1759</strong>&nbsp;(Jawa) secara resmi ditetapkan
                pembentukan&nbsp;<strong>Kabupaten Bantul</strong>&nbsp;yang sebelumnya di kenal
                bernama&nbsp;<strong>Bantulkarang</strong>. Seorang Nayaka Kasultanan Yogyakarata
                bernama&nbsp;<strong>Raden Tumenggung Mangun Negoro</strong>&nbsp;kemudian dipercaya Sri Sultan
                Hamengkubuwono V untuk memangku jabatan sebagai Bupati Bantul.</p>
              <p style="text-align: justify;">
                Tanggal 20 Juli ini lah yang setiap tahunnya diperingati sebagai&nbsp;<strong>Hari Jadi Kabupaten
                  Bantul</strong>. Selain itu tanggal 20 Juli tersebut juga memiliki nilai simbol kepahlawanan dan
                kekeramatan bagi masyarakat Bantul mengingat Perang Diponegoro dikobarkan tanggal 20 Juli 1825.Pada
                masa pendudukan Jepang, pemerintahan berdasarkan pada&nbsp;<strong>Usamu Seirei</strong>nomor 13
                sedangakan&nbsp;<strong>stadsgemente ordonantie</strong>&nbsp;dihapus. Kabupaten Memiliki hak
                mengelola rumah tangga sendiri (otonom).</p>
              <p style="text-align: justify;">
                Kemudian setelah kemerdekaan, pemerintahan ditangani oleh Komite Nasional Daerah untuk melaksanakan UU
                No 1 tahun 1945. Tetapi di Yogyakarta dan Surakarta undang-undang tersebut tidak diberlakukan hingga
                dikeluarkannya UU Pokok Pemerintah Daerah No 22 tahun 1948. dan selanjutnya mengacu UU Nomor 15 tahun
                1950 yang isinya pembentukan Pemerintahan Daerah Otonom di seluruh Indonesia.</p>
              <p style="text-align: justify;">
                Seiring dengan perjalanan Negara Kesatuan Republik Indonesia dan silih bergantinya kepemimpinan
                nasional, kini ini Kabupaten Bantul telah mengalami kemajuan pesat diberbagai bidang dibawah
                kepemimpinan Drs. HM. Idham Samawi yang menjabat sejak akhir tahun 1999.</p>
